package org.lightjason.agentspeak.action.graph;

import edu.uci.ics.jung.graph.Graph;
import org.lightjason.agentspeak.action.IBaseAction;
import org.lightjason.agentspeak.common.IPath;
import org.lightjason.agentspeak.error.context.CExecutionIllegealArgumentException;
import org.lightjason.agentspeak.language.CCommon;
import org.lightjason.agentspeak.language.CRawTerm;
import org.lightjason.agentspeak.language.ITerm;
import org.lightjason.agentspeak.language.execution.IContext;
import org.lightjason.agentspeak.language.fuzzy.IFuzzyValue;

import javax.annotation.Nonnegative;
import javax.annotation.Nonnull;
import java.util.List;
import java.util.stream.Collectors;
import java.util.stream.Stream;


/**
 * check if a graph contains a vertex.
 * The action checks for the first argument,
 * if each other graph argument contains the
 * first item as an vertex
 *
 * {@code [B1|B2] = .graph/containsvertex( Element, Graph1, Graph2 );}
 */
public final class CContainsVertex extends IBaseAction
{
    /**
     * serial id
     */
    private static final long serialVersionUID = -7405732178225852719L;
    /**
     * action name
     */
    private static final IPath NAME = namebyclass( CContainsVertex.class, "graph" );

    @Nonnull
    @Override
    public IPath name()
    {
        return NAME;
    }

    @Nonnegative
    @Override
    public int minimalArgumentNumber()
    {
        return 1;
    }

    @Nonnull
    @Override
    public Stream<IFuzzyValue<?>> execute( final boolean p_parallel, @Nonnull final IContext p_context,
                                           @Nonnull final List<ITerm> p_argument, @Nonnull final List<ITerm> p_return
    )
    {
        final List<ITerm> l_arguments = CCommon.flatten( p_argument ).collect( Collectors.toList() );
        if ( l_arguments.size() < 2 )
            throw new CExecutionIllegealArgumentException(
                p_context, org.lightjason.agentspeak.common.CCommon.languagestring( this, "wrongargumentnumber", 3 ) );

        l_arguments.stream()
                   .skip( 1 )
                   .map( ITerm::<Graph<Object, Object>>raw )
                   .map( i -> i.containsVertex( l_arguments.get( 0 ).raw() ) )
                   .map( CRawTerm::of )
                   .forEach( p_return::add );

        return Stream.of();
    }
}
